// important points for FINAL Keyword

/*
 * final keyword: ============== we can use final keyword in three ways
 * 
 * 1)We can a declare a final variable ,along with declaration we must
 * initialize that final variable 2)final variable will act as constant i.e.,
 * value of final variable cannot be changed(fixed)
 * 
 * 3)we can declare a method as final by using final keyword note:-final method
 * will not participate in method overriding.
 * 
 * 4)we can declare a class as final and we can create the object for final
 * class but final class will not participate in inheritance.
 */